good_files="good/*"
echo "==========================================================="
echo "=========================CORRECT==========================="
echo "==========================================================="
for good in $good_files
do
	MIML/Test $good
	if [[ $? -ne 0 ]]; then
		echo "Failed on correct program"
    	exit 1
	fi
done

bad_files="bad/*"
echo "==========================================================="
echo "==========================WRONG============================"
echo "==========================================================="
for bad in $bad_files
do
	MIML/Test $bad
	if [[ $? -ne 1 ]]; then
		echo "Parsed incorrect program"
    	exit 1
	fi
done

echo "==========================================================="
echo "========================SUCCESS============================"
echo "==========================================================="